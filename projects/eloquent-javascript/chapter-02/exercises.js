function triangles(input){
  var output = "";
  for(var i = 0; i < input; i++){
    output += "#";
    console.log(output);
  }
}

////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
  for (let number = 1; number <= 15; number++) {
    if(number % 3 === 0 && number % 5 === 0){
      console.log("fizzbuzz");
    } else if(number % 3 === 0){
      console.log("fizz");
    } else if(number % 5 === 0){
      console.log("buzz");
    } else {
      console.log(number);
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(size) {
  var board = "";
  for(let y = 0; y < size; y++){
    for(let x = 0; x < size; x++){
      if((x + y) % 2 == 0){
        board += " ";
      } else {
        board += "#";
      }
    }
  board += "\n";
  }
  console.log(board);
}

