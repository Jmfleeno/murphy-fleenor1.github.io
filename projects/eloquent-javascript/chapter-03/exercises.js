////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
	if (n < 0) {
		return undefined;
	} else {	
		if (n == 0) {
			return true;
		}
		if (n == 1) {
			return false;
		}
		return isEven(n - 2);
	}
}

