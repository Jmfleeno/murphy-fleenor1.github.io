////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

var min = function(x, y) {
  return (x <= y) ? x : y;
};

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

var isEven = function(num) {
  num = Math.abs(num);
  if (num === 0)
    return true;
  else if (num === 1)
    return false;
  else
    return isEven(num - 2);
};

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

var countChars = function(str, char) {
    var count = 0;
    for (var i = 0; i < str.length; i++) {
      if (str[i] === char){
        count += 1;
      }
    }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

var countBs = function(str) {
  if (!str){
    return 0;
  }
  return str.match(/B/g).length;
};

